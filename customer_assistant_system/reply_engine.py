#关键词分类规则,顺序就是优先级
CLASSIFY_RULES = [
("退款","退款问题"),
("物流","物流问题"),
("快递","物流问题"),
("包装","包装问题"),
("破损","包装问题"),
("质量","产品质量问题"),
("瑕疵","产品质量问题"),
("开裂","产品质量问题"),
]

#分类对应的回复话术
REPLY_MAP = {
    "物流问题":   "您好,我们正在梳实物流轨迹,会第一时间同步进度给您.",
    "包装问题":   "您好,非常抱歉包装出现损坏,售后专员会在1小时内联系您补发或理赔",
    "退款问题":   "您好,正在查询退款进度,财务对账后原路退回,预计1-3个工作日到账",
    "产品质量问题":  "您好,产品质量问题我们高度重视,支持免费换新+运费全包.稍后售后对接方案",
    "其它问题":   "您好,您的诉求已完整记录.专员会在工作时间内联系您处理",
}

def classify (text):
    """根据关键词判断投诉分类"""
    for keyword, category in CLASSIFY_RULES:
        if keyword in text:
            return category
    return "其它问题"


def get_reply(text):
    """传入投诉内容,返回(分类,回复话术)"""
    category = classify(text)
    reply = REPLY_MAP[category]
    return category, reply
